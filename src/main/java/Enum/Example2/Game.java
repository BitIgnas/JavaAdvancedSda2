package Enum.Example2;

public class Game {
    private String name;
    private GameLevel gameLevel;

    public void setName(String name) {
        this.name = name;
    }

    public void setGameLevel(GameLevel gameLevel) {
        this.gameLevel = gameLevel;
    }

    public String getName() {
        return name;
    }

    public GameLevel getGameLevel() {
        return gameLevel;
    }


}
